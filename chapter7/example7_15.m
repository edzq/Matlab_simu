function [pb,ps]=snr2p(snr_in_dB)
% [pb,ps]=snr2p(snr_in_dB)
%???dB?????????????????????
N=10000; %???
Es=1;				  	
snr=10^(snr_in_dB/10);	 	  	%????????
sgma=sqrt(Es/(4*snr));	  	  	%???????
% ????
s00=[1 0];
s01=[0 1];
s11=[-1 0];
s10=[0 -1];
%?????
for i=1:N,	
  temp=rand;			  	%?????0?1??????????
  if (temp<0.25),		  	%????"00"????1/4 
    dsource1(i)=0;
    dsource2(i)=0;		   
  elseif (temp<0.5),		  	%????"01"????1/4
    dsource1(i)=0;
    dsource2(i)=1;
  elseif (temp<0.75),	 	  	%????"10"????1/4
    dsource1(i)=1;	
    dsource2(i)=0;
  else			          	%????"11"????1/4
    dsource1(i)=1;
    dsource2(i)=1;
  end;
end;
%????????
numofsymbolerror=0;
numofbiterror=0;
for i=1:N,
  %???????????????i?????
  n(1)=bmgauss(sgma);	  	  
  n(2)=bmgauss(sgma);
  if ((dsource1(i)==0) & (dsource2(i)==0)),
    r=s00+n;    %??00?????????
  elseif ((dsource1(i)==0) & (dsource2(i)==1)),
    r=s01+n;    %??01?????????
  elseif ((dsource1(i)==1) & (dsource2(i)==0)),
    r=s10+n;      %??10?????????
  else
    r=s11+n;      %??11?????????
  end;
  % ??????????
  c00=dot(r,s00);
  c01=dot(r,s01);
  c10=dot(r,s10);
  c11=dot(r,s11);
  % ?i??????????
  c_max=max([c00 c01 c10 c11]);
  if (c00==c_max),
    decis1=0; decis2=0;
  elseif (c01==c_max),
    decis1=0; decis2=1;
  elseif (c10==c_max),
    decis1=1; decis2=0;
  else
    decis1=1; decis2=1;
  end;
  %???????????????1
  symbolerror=0;
  if (decis1~=dsource1(i)),
    numofbiterror=numofbiterror+1;
    symbolerror=1;
  end;
  if (decis2~=dsource2(i)),
    numofbiterror=numofbiterror+1;
    symbolerror=1;
  end;
  if (symbolerror==1),
    numofsymbolerror = numofsymbolerror+1;
  end;
end;
ps=numofsymbolerror/N;	          	% ????N???
pb=numofbiterror/(2*N);    	  	%????2N???
end